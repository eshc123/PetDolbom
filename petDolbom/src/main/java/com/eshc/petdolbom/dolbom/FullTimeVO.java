package com.eshc.petdolbom.dolbom;


public class FullTimeVO {
	
	private int full_service_idx;
	private String member_id;
	private String cared_pet;
	private int full_service_status;
	private String name;
	private String address;
	public int getFull_service_idx() {
		return full_service_idx;
	}
	public void setFull_service_idx(int full_service_idx) {
		this.full_service_idx = full_service_idx;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getCared_pet() {
		return cared_pet;
	}
	public void setCared_pet(String cared_pet) {
		this.cared_pet = cared_pet;
	}
	public int getFull_service_status() {
		return full_service_status;
	}
	public void setFull_service_status(int full_service_status) {
		this.full_service_status = full_service_status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}

	
	
}
